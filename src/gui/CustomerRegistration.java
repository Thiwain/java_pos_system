    private void sortAndSearch() {
        int sortIndex = jComboBox1.getSelectedIndex();
        String mobile = jTextField1.getText();

        if (sortIndex == 0) {
            loadCustomer("first_name", "ASC", mobile);
        } else if (sortIndex == 1) {
            loadCustomer("first_name", "DESC", mobile);
        } else if (sortIndex == 2) {
            loadCustomer("points", "ASC", mobile);
        } else if (sortIndex == 3) {
            loadCustomer("points", "DESC", mobile);
        }
    }

