    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String mobile = String.valueOf(jTextField1.getText());
        String fname = String.valueOf(jTextField2.getText());
        String lname = String.valueOf(jTextField3.getText());
        String email = String.valueOf(jTextField4.getText());

        if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the Mobile Number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!mobile.matches("^07[012345678]{1}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "Invalid Mobile Number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "First Name is Empty", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Last Name is Empty", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter employee email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!email.matches("^(.+)@(.+)$")) {
            JOptionPane.showMessageDialog(this, "Invalid employee email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {

                ResultSet resultset = MySQL.execute("SELECT * FROM `customer` WHERE `mobile`='" + mobile + "' OR `email`='" + email + "'");

                if (resultset.next()) {
                    JOptionPane.showMessageDialog(this, "Customer Already Exists", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.execute("INSERT INTO `customer`(`mobile`,`first_name`,`last_name`,`email`,`points`) "
                            + "VALUES('" + mobile + "','" + fname + "','" + lname + "','" + email + "','0')");

                }
            } catch (Exception e) {
//                e.printStackTrace();
                logger.log(Level.WARNING, "Customer Registration Error" + formattedDateTime, e);
            }
        }

        reset();
        loadCustomer("first_name", "ASC", "0");

    }//GEN-LAST:event_jButton1ActionPerformed

