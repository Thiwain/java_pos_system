package model;

/**
 *
 * @author Senut
 */
public class InvoiceItem {

    private static String stockId;
    private static String brand;
    private static String name;
    private static String qty;
    private static String sellingPrice;
    private static String mfg;
    private static String exp;

    /**
     * @return the stockId
     */
    public static String getStockId() {
        return stockId;
    }

    /**
     * @param aStockId the stockId to set
     */
    public static void setStockId(String aStockId) {
        stockId = aStockId;
    }

    /**
     * @return the brand
     */
    public static String getBrand() {
        return brand;
    }

    /**
     * @param aBrand the brand to set
     */
    public static void setBrand(String aBrand) {
        brand = aBrand;
    }

    /**
     * @return the name
     */
    public static String getName() {
        return name;
    }

    /**
     * @param aName the name to set
     */
    public static void setName(String aName) {
        name = aName;
    }

    /**
     * @return the qty
     */
    public static String getQty() {
        return qty;
    }

    /**
     * @param aQty the qty to set
     */
    public static void setQty(String aQty) {
        qty = aQty;
    }

    /**
     * @return the sellingPrice
     */
    public static String getSellingPrice() {
        return sellingPrice;
    }

    /**
     * @param aSellingPrice the sellingPrice to set
     */
    public static void setSellingPrice(String aSellingPrice) {
        sellingPrice = aSellingPrice;
    }

    /**
     * @return the mfg
     */
    public static String getMfg() {
        return mfg;
    }

    /**
     * @param aMfg the mfg to set
     */
    public static void setMfg(String aMfg) {
        mfg = aMfg;
    }

    /**
     * @return the exp
     */
    public static String getExp() {
        return exp;
    }

    /**
     * @param aExp the exp to set
     */
    public static void setExp(String aExp) {
        exp = aExp;
    }

    

}
